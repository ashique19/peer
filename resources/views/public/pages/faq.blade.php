                <h2>What is a carrying fee?</h2>
                <p>
                    A carrying fee is the amount of money a buyer pays a traveler
                    for buying and delivering product(s) to them.
                </p>
                <h2>How is a carrying fee determined?</h2>
                <p>
                    Airposted has no involvement in determining a carrying fee - it
                    is completely negotiated between the buyer and traveler. If
                    either the buyer or traveler sends an offer the other party
                    doesn’t like, they are able to send a counter offer until they have
                    come to an agreed price. We recommend travelers determine
                    the carrying fee they will charge based on factors such as
                    product size, weight, price and ease in which the traveler is able
                    to obtain the product. Depending on the product, there may be
                    VAT, customs and local shipping fees involved with transporting
                    the product as well. We recommend buyers keep these same
                    factors in mind when negotiating the carrying fee.
                </p>
